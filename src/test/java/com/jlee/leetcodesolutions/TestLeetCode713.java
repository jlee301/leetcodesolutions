  @Test
  public void testProblemCase() {
    int[] nums = {10, 5, 2, 6};
    int k = 100;
    LeetCode713 solution = new LeetCode713();
    Assert.assertEquals(8, solution.numSubarrayProductLessThanK(nums, k));
  }
  
  @Test
  public void testWrongAns() {
    int[] nums = {10,9,10,4,3,8,3,3,6,2,10,10,9,3};
    int k = 19;
    LeetCode713 solution = new LeetCode713();
    Assert.assertEquals(18, solution.numSubarrayProductLessThanK(nums, k));
  }  

